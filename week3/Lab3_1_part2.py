# Lab 3.1 Part 2: Full Multiplication Grid (12x12)

print("=== ตารางสูตรคูณเต็มรูปแบบขนาด 12x12 ===")
print()

# ลูปวงนอก (Outer loop) วนรอบตามแถว (Rows) จาก 1 ถึง 12
for i in range(1, 13):
    
    # ลูปวงใน (Inner loop) วนรอบตามคอลัมน์ (Columns) จาก 1 ถึง 12
    for j in range(1, 13):
        
        # คำนวณผลคูณ
        result = i * j
        
        # พิมพ์ผลคูณออกมาโดยใช้ฟอร์แมต :4d (จองพื้นที่ 4 ช่อง และชิดขวา)
        # ใช้ end="" เพื่อไม่ให้ขึ้นบรรทัดใหม่หลังพิมพ์เสร็จในแต่ละคอลัมน์
        print(f"{result:4d}", end="")
        
    # เมื่อพิมพ์ครบ 12 คอลัมน์ในแถวนั้นแล้ว ให้สั่ง print() เปล่าๆ เพื่อขึ้นบรรทัดใหม่สำหรับแถวถัดไป
    print()